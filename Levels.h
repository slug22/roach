#include <vector>

// ============================================================================
// PORTAL DEF
// ============================================================================
struct PortalDef {
    int x;              // grid position
    int y;
    int portalID;       // logical ID (2,3,4,...)
    int targetLevel;    // level index
    int targetPortalID; // portal ID you spawn at in target level
};

// ============================================================================
// LEVEL DEF
// ============================================================================
struct LevelDef {
    int (*tiles)[25];                // pointer to 2D array of 18x25 tiles
    std::vector<PortalDef> portals;  // all portals in this level
};

